    private void sendButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendButtonActionPerformed
    if(sendToTextField.getText().equals("") || txtSubject.getText().equals("")|| txtEmailField.getText().equals(""))
        {
        JOptionPane.showMessageDialog(null,new JLabel(  "<html><h2><I>Please Fill in</I><font color='pink'> AllThe Fields</font>!</h2></html>"),"Error!!!",JOptionPane.ERROR_MESSAGE);
        }
    else{
    
    String toEmailid = sendToTextField.getText();
    String fromEmailid = sendFromTextField.getText();
    String fromEmailPassword = "SreePreetiAnkur";
    String EmailSubject = txtSubject.getText();    
    Properties p = new Properties();
    p.put("mail.smtp.auth","true");
    p.put("mail.smtp.starttls.enable","true");
    p.put("mail.smtp.host","smtp.gmail.com");
    p.put("mail.smtp.port","587");
        
    Session session = Session.getDefaultInstance(p,new javax.mail.Authenticator() {
    protected PasswordAuthentication getPasswordAuthentication(){
    return new PasswordAuthentication(fromEmailid, fromEmailPassword);
            }
        });
        
        try{
            MimeMessage message = new MimeMessage(session);
            message.setFrom(new InternetAddress(fromEmailid));
            message.addRecipient(Message.RecipientType.TO, new InternetAddress(toEmailid));
            message.setSubject(txtSubject.getText());
            message.setText(txtEmailField.getText());
            Transport.send(message);
              
     JOptionPane.showMessageDialog(null,new JLabel(  "<html><h2><I> Your Email Has been Sent</I><font color='pink'> </font>!</h2></html>"),"Success!!!",JOptionPane.ERROR_MESSAGE);
        WorkArea.remove(this);
        Component[] componentArray = WorkArea.getComponents();
        Component component = componentArray[componentArray.length - 1];
        CardLayout layout = (CardLayout)WorkArea.getLayout();
        layout.previous(WorkArea);
            
        }catch(Exception ex){
            System.out.println(""+ex);
              
     JOptionPane.showMessageDialog(null,new JLabel(  "<html><h2><I> Your Email Has Failed</I><font color='pink'> </font>!</h2></html>"),"Failed!!!",JOptionPane.ERROR_MESSAGE);
     
        }
   
    }                
    }//GEN-LAST:event_sendButtonActionPerformed

