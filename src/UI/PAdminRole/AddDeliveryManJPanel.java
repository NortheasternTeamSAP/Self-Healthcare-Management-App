    private void btnCreate6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreate6ActionPerformed
        // TODO add your handling code here:
        if( txtName.getText().isEmpty() ||
            txtDOB.getText().isEmpty() ||  //later add for rbtns
            txtBuildingNo.getText().isEmpty() || 
            txtStreet.getText().isEmpty() || 
            txtCity.getText().isEmpty() || 
            txtZIP.getText().isEmpty() || 
            txtState.getText().isEmpty() || 
            txtCountry.getText().isEmpty() || 
            txtPhoneNumber6.getText().isEmpty() || 
            txtUsername6.getText().isEmpty() || 
            txtPassword6.getText().isEmpty()) {
           
            JOptionPane.showMessageDialog(null, "Fields cannot be left empty");
            return;
        }

        String fullName = txtName.getText();
        LocalDate dob = LocalDate.parse(txtDOB.getText()); //parsing string to LocalDate for now. Later add jcalendar.
        String street = txtStreet.getText();
        String apartment = txtBuildingNo.getText();
        String city = txtCity.getText();
        String zipcode = txtZIP.getText();
        String state = txtState.getText();
        String country = txtCountry.getText();
        
        boolean flag; 
        //add other validations too
        
        flag = fullName.matches("^[a-zA-Z]+$");

        if(flag == false) {
            JOptionPane.showMessageDialog(null, "Full name cannot have integer values!");
            return;
        }
        
        String phoneNumber = txtPhoneNumber6.getText();

        if(phoneNumber.length() != 10) {
            JOptionPane.showMessageDialog(null, "Phone Number must consist of 10 digits");
            return;
        }
        flag = phoneNumber.matches("^[0-9]+$");
        if(!flag) {
            JOptionPane.showMessageDialog(null, "Phone Number must have digits only");
            return;
        }
        for(DeliveryMan deliveryMan : DeliveryManDirectory.getDeliveryManMap()) { //check
            if(deliveryMan.getPhoneNumber().equals(phoneNumber)) {
                JOptionPane.showMessageDialog(null, "This Phone Number exists!");
                return;
            }
        }
       
        String userName = txtUsername6.getText();
        String password = txtPassword6.getText();

        for(UserAccount account : ecoSystem.getUserAccountDirectory().getUserAccountList()) {
            if(account.getUsername().equals(userName)) {
                JOptionPane.showMessageDialog(null, "This username exists!");
                return;
            }
        }

        //need to add directories to EcoSystem class
        ecoSystem.getDeliveryManDirectory().addDeliveryMan(fullName, dob, street, apartment, city, zipcode, state, country); 
        Employee employee = ecoSystem.getEmployeeDirectory().createEmployee(email);
        ecoSystem.getUserAccountDirectory().createUserAccount(userName, password, employee, new DeliveryManRole());

        JOptionPane.showMessageDialog(null, "Your profile has been successfully created!");
    }//GEN-LAST:event_btnCreate6ActionPerformed

