    private void btnSaveMedicineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveMedicineActionPerformed
        // TODO add your handling code here:
        try {

            if((txtUpdateMedicinePrice.getText().isEmpty()) || (Double.parseDouble(txtUpdateMedicinePrice.getText()) <= 0)){

                JOptionPane.showMessageDialog(this, "Please add valid price of Medicine! Price must be greater than 0.");
                return;
            }
            Medicine medicine = ecoSystem
                    .getMedicineDirectory()
                    .getMedicineCatalogMap()
                    .get(txtUpdateMedicineID
                            .getText()
                    );
            medicine.setPrice(Double.parseDouble(txtUpdateMedicinePrice.getText()));
            JOptionPane.showMessageDialog(null, "Medicine updated successfully!!", "Info", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showInternalMessageDialog(this, "Kindly fill in the details in correct format.");
        }
        
    }//GEN-LAST:event_btnSaveMedicineActionPerformed


