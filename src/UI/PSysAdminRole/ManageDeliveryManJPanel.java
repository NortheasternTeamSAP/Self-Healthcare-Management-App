    public void populateDeliveryManDetailsTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblMngDeliveryMan.getModel();
        dtm.setRowCount(0);
        
        for(DeliveryMan deliveryMan : ecoSystem.getDeliveryManDirectory().getDeliveryManMap().getAllValues()){
            Object [] row = new Object[4];
            row[0] = deliveryMan.getPersonDetails().getFullName();
            row[1] = deliveryMan.getDeliveryManDetails().getAddress();
            row[2] = deliveryMan.getDeliveryManDetails().getPhoneNumber();
            row[3] = deliveryMan.getDeliveryManDetails().getDob();
          
            dtm.addRow(row);
        }
    }

