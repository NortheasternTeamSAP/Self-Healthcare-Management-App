        DeliveryMan deliveryMan = (DeliveryMan) tblMngDeliveryMan.getValueAt(selectedRow, 0);
        ecoSystem.getDeliveryManDirectory().deleteDeliveryMan(deliveryMan);
        populateDeliveryManDetailsTable();
