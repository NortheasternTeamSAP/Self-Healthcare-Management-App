    private void btnUpdateDeliveryManActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateDeliveryManActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMngDeliveryMan.getSelectedRow();
        if(selectedRow < 0) {
            JOptionPane.showMessageDialog(null,"Select a row from table.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        DeliveryMan deliveryMan = (DeliveryMan)tblMngDeliveryMan.getValueAt(selectedRow,0);
        UpdateDeliveryManJPanel updateDeliveryManJPanel = new UpdateDeliveryManJPanel(workArea, ecoSystem, deliveryMan);
        workArea.add("ModifyDeliveryManJPanel",updateDeliveryManJPanel);
        CardLayout layout =(CardLayout)workArea.getLayout();
        layout.next(workArea);
    }//GEN-LAST:event_btnUpdateDeliveryManActionPerformed

