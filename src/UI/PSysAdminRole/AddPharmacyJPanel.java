    private void populateCmbPharmacyPharmacistName() {                                                          
        // TODO add your handling code here:
        cmbPharmacyPharmacistName.removeAllItems();
        for (Person p: ecoSystem.globalUserDirectory.getByRole(Role.PHARMACIST)){
            cmbPharmacyPharmacistName.addItem(p);
        }
    }                                                         

