    private void btnAddPharmacyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddPharmacyActionPerformed

        try {

            if( txtAddPharmacyName.getText().isEmpty() ||
                txtAddPharmacyBuildingNo.getText().isEmpty() || 
                txtAddPharmacyStreet.getText().isEmpty() || 
                txtAddPharmacyCity.getText().isEmpty() || 
                txtAddPharmacyZIP.getText().isEmpty() || 
                txtAddPharmacyState.getText().isEmpty() || 
                txtAddPharmacyCountry.getText().isEmpty() || 
                txtPharmacyUsername.getText().isEmpty() || 
                txtPharmacyPassword.getText().isEmpty()){

                JOptionPane.showMessageDialog(this, "Please add details of Pharmacy! Fields cannot be empty.");
                return;
            }
            
            
            Address address = new Address(
                    txtAddPharmacyStreet.getText(),
                    txtAddPharmacyBuildingNo.getText(),
                    txtAddPharmacyCity.getText(),
                    txtAddPharmacyZIP.getText(),
                    txtAddPharmacyState.getText(),
                    txtAddPharmacyCountry.getText()
            );
            
            PharmacyEnterprise pharmacy = ecoSystem.getPharmacyDirectory().addPharmacy(txtAddPharmacyName.getText(),address);
            JOptionPane.showMessageDialog(null, "Pharmacy added successfully!!", "Info", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            JOptionPane.showInternalMessageDialog(this, "Kindly fill in the details in correct format.");
        }

    }//GEN-LAST:event_btnAddPharmacyActionPerformed

